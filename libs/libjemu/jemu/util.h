#pragma once

#include <stdint.h>

namespace jemu {

inline static uint32_t nextPowerOfTwo (uint32_t size)
{
    // http://graphics.stanford.edu/~seander/bithacks.html#RoundUpPowerOf2
    size--;
    size |= size >> 1;
    size |= size >> 2;
    size |= size >> 4;
    size |= size >> 8;
    size |= size >> 16;
    size++;
    return size;
}

}
